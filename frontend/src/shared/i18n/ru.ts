/** Russian plural form for "книга" */
export function pluralBooks(count: number): string {
  const n = Math.abs(count) % 100;
  const n1 = n % 10;
  if (n > 10 && n < 20) return ru.common.booksMany;
  if (n1 > 1 && n1 < 5) return ru.common.books;
  if (n1 === 1) return ru.common.book;
  return ru.common.booksMany;
}

export function pluralRecords(count: number): string {
  const n = Math.abs(count) % 100;
  const n1 = n % 10;
  if (n > 10 && n < 20) return ru.common.recordsMany;
  if (n1 > 1 && n1 < 5) return ru.common.records;
  if (n1 === 1) return ru.common.record;
  return ru.common.recordsMany;
}

export function pluralUsers(count: number): string {
  const n = Math.abs(count) % 100;
  const n1 = n % 10;
  if (n > 10 && n < 20) return ru.common.usersMany;
  if (n1 > 1 && n1 < 5) return ru.common.users;
  if (n1 === 1) return ru.common.user;
  return ru.common.usersMany;
}
